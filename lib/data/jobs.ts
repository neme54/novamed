export async function getJobs(): Promise<JobListing[]> {
  return jobs.slice().sort((a, b) => a.title.localeCompare(b.title));
}

export async function getJobBySlug(slug: string): Promise<JobListing | null> {
  const all = await getJobs();
  return all.find((x) => x.slug === slug) ?? null;
}

export async function getDepartments(): Promise<string[]> {
  const all = await getJobs();
  return Array.from(new Set(all.map((x) => x.department))).sort();
}

